package unit6_loops;

public class for_loops {
    public static void main(String[] args) {
        for (int counter = 0; counter < 10; counter++) {
            System.out.println("Test");
        }

        System.out.println();

        for (int i = 1250; i > 400; i = i - 50) {
            System.out.println(i);
        }
    }
}
